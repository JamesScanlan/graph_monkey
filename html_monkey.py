def write_html_start():
    return '<!DOCTYPE html><html>'

def write_html_end():
    return '</html>'

def write_head_start():
    return '<head>'

def write_head_end():
    return '</head>'

def write_style_sheet_reference(stylesheet_name):
    return '<link rel="stylesheet" type="text/css" href="' + stylesheet_name + '">'

def write_body_start():
    return '<body>'

def write_body_end():
    return '</body>'